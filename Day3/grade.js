let grade = "C";

switch (grade) {
    case "A":
        console.log("This grade is excellent")
        break;
    case "B":
        console.log("This grade is good")
        break;
    case "C":
        console.log("This grade is average")
        break;
    case "D":
        console.log("This grade is below average")
        break;
    case "F":
        console.log("This grade is poor")
        break;
}